for CANID in 1 2 3 4
do
    echo "Mapping Position and Velocity to PDO3 for motor $CANID"
	COBID=$(( $CANID + 600 ))

	#DISABLE PDO
	cansend can0 $COBID#2F.02.18.01.01.05.00.80
	#DISABLE TPDO3 	MAPPING
	cansend can0 $COBID#2F.02.1A.00.00.00.00.00

	#MAP POSITION TO 1ST INDEX
	cansend can0 $COBID#2F.02.1A.01.20.00.63.60
	#MAP VELOCITY TO 2ND INDEX
	cansend can0 $COBID#2F.02.1A.02.20.00.69.60

	#SET PDO3 TRANSMISSION TYPE TO ASYNCH
	cansend can0 $COBID#2F.02.18.02.FF.00.00.00
	#SET EVENT TIMER TO 50MS
	cansend can0 $COBID#2F.02.18.05.32.00.00.00

	#ACTIVATE PDO MAPPING
	cansend can0 $COBID#2F.02.1A.00.02.00.00.00
	#ENABLE PDO
	cansend can0 $COBID#2F.02.18.01.01.04.00.00
done